package ultimate.java.progs;

public class Fuss {

	private int id;
	private String fussName;
	
	public Fuss(int id, String fussName) {
		super();
		this.id = id;
		this.fussName = fussName;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFussName() {
		return fussName;
	}
	public void setFussName(String fussName) {
		this.fussName = fussName;
	}
	
	
}
